  ActionView::Base.default_form_builder = TailwindFormBuilder

  def gettext(text)
    text
  end

  def item_class(action, kind)
    if action == kind
      "dark:bg-black dark:text-gray-100 bg-gray-50 bg-opacity-50 flex p-6 border-b border-gray-gray-200 dark:border-gray-800"
    else
      "dark:bg-gray-900 dark:text-gray-100 hover:bg-gray-50 hover:bg-opacity-50 flex p-6 border-b border-blue-gray-200 dark:border-gray-800"
    end
  end

  def categories_section(slug)
    Post.where(category: Category.friendly.find(slug))
  end

  def active_tab_for?(current_tab, expected)
    if current_tab == expected
      "block"
    else
      "hidden"
    end
  end

  def event_status_label(event)
    case event.state
    when "published" then gettext("Your event is published")
    else  
      gettext("Your event has not been published")
    end
  end

  def button_label(event)
    case event.state
    when "published" then gettext("Unpublish Event")
    else  
      gettext("Publish Event")
    end
  end

  def active_tab_link?(current_tab, expected)
    if current_tab == expected
      "bg-brand-100 text-brand-700"
    else
      "text-brand-500 hover:text-brand-700"
    end
  end

